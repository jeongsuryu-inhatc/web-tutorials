// sample1.js — Hello World와 console.log

// console.log()는 괄호 안의 내용을 터미널 화면에 출력하는 명령이다.
console.log("Hello, World!");

// 문자열(따옴표로 감싼 글자)뿐 아니라 숫자도 바로 출력할 수 있다.
console.log(2026);

// 쉼표로 여러 값을 나열하면 한 줄에 이어서 출력된다.
console.log("내 이름은", "홍길동", "입니다.");

// 한 줄 주석은 // 로 시작한다. 이 줄은 실행되지 않는다.
/*
  여러 줄 주석은 이렇게
  슬래시-별표 로 시작해서 별표-슬래시 로 끝낸다.
*/

// 실행 방법: 터미널에서 아래 명령을 입력한다.
// node sample1.js
