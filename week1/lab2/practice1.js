// practice1.js — 구구단 출력기
//
// 목적: 중첩 반복문(for 안에 for)을 사용해 2단부터 9단까지
//       구구단 전체를 출력하는 프로그램을 작성한다.
//
// 요구사항:
// 1. 바깥쪽 반복문은 2~9 (단), 안쪽 반복문은 1~9 (곱하는 수)를 돈다.
// 2. 각 줄은 "2 x 1 = 2" 형태로 출력한다. (템플릿 리터럴 사용 권장)
// 3. 각 단이 끝날 때마다 빈 줄을 한 번 출력해 구분한다.
//
// 실행: node practice1.js

for (let dan = 2; dan <= 9; dan++) {
  for (let i = 1; i <= 9; i++) {
    console.log(`${dan} x ${i} = ${dan * i}`);
  }
  console.log(""); // 단이 끝날 때마다 빈 줄
}
