// practice7.js — 별 찍기 패턴
//
// 목적: 중첩 반복문과 문자열 이어붙이기를 사용해
//       삼각형 모양의 별(★) 패턴을 출력하는 프로그램을 작성한다.
//
// 요구사항:
// 1. rows(예: 5)를 입력값으로 정한다.
// 2. 1번째 줄에는 별 1개, 2번째 줄에는 별 2개, ... rows번째 줄에는
//    별 rows개가 출력되도록 만든다. (직각삼각형 모양)
//    예시 (rows = 5):
//    ★
//    ★★
//    ★★★
//    ★★★★
//    ★★★★★
// 3. 반복문 안에서 문자열을 계속 이어 붙인 뒤 한 줄에 한 번만 출력한다.
// 4. 여유가 있다면 rows 값을 바꿔가며 결과가 잘 바뀌는지 확인한다.
//
// 실행: node practice7.js

const rows = 5;

for (let i = 1; i <= rows; i++) {
  let line = "";
  for (let j = 0; j < i; j++) {
    line += "★";
  }
  console.log(line);
}
